    private static Counterparty create(String id, String name, String city, String region, String index, String address) {
        return Counterparty.builder()
                .positionNumber(id)
                .name(name)
                .city(city)
                .region(region)
                .regionId(getRegionId(region))
                .index(index)
                .address(address)
                .build();

    }


    private static Integer getRegionId(String region){
        return regions().get(region);
    }


